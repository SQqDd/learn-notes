const arr1 = [1, 2, 3, 4, 5]

console.log(arr1.slice(0, 3)) // [1, 2 , 3]
console.log(arr1.slice(0, 3)) // [1, 2 , 3]
console.log(arr1.slice(0, 3)) // [1, 2 , 3]

const arr2 = [1, 2, 3, 4, 5]

console.log(arr2.splice(0, 3)) // [1, 2 , 3]
console.log(arr2.splice(0, 3)) // [4, 5]
console.log(arr2.splice(0, 3)) // []
